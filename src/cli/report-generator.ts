/**
 * Generate a summary Markdown report for bulk backward analysis
 */
export function generateBulkMarkdownReport(report: BulkBackwardReport): string {
  const lines: string[] = [];

  lines.push('# Backward Analysis Report');
  lines.push('');
  lines.push(`**Generated**: ${report.timestamp}`);
  if (report.model) {
    lines.push(`**Model**: ${report.model}`);
  }
  lines.push(`**Source**: ${report.sourceRepo}`);
  lines.push(`**Target**: ${report.targetRepo} (${report.language})`);
  lines.push('');

  // Summary table
  lines.push('## Summary');
  lines.push('');
  lines.push(`| Metric | Count |`);
  lines.push(`|--------|-------|`);
  lines.push(`| Files analyzed | ${report.filesAnalyzed} |`);
  lines.push(`| Files in sync | ${report.filesInSync} |`);
  lines.push(`| Files with suggestions | ${report.filesFlagged} |`);
  lines.push(`| Files skipped (too large) | ${report.filesSkipped} |`);
  lines.push(`| Total suggestions | ${report.totalSuggestions} |`);
  lines.push(`| High confidence (≥0.85) | ${report.highConfidence} |`);
  lines.push(`| Medium confidence (0.6-0.85) | ${report.mediumConfidence} |`);
  lines.push(`| Low confidence (<0.6) | ${report.lowConfidence} |`);
  lines.push('');

  // Per-file details (only files with suggestions)
  const filesWithSuggestions = report.fileReports.filter(
    f => f.suggestions.some(s => s.recommendation === 'BACKPORT')
  );

  if (filesWithSuggestions.length > 0) {
    lines.push('## Files with Suggestions');
    lines.push('');

    for (const fileReport of filesWithSuggestions) {
      const suggestions = fileReport.suggestions.filter(s => s.recommendation === 'BACKPORT');
      lines.push(`### ${fileReport.file} (${suggestions.length} suggestion${suggestions.length === 1 ? '' : 's'})`);
      lines.push('');

      for (const suggestion of suggestions) {
        lines.push(formatSuggestion(suggestion));
        lines.push('');
      }
    }
  }

  lines.push('---');
  lines.push('');
  lines.push('*This report was generated by `resync backward`. Suggestions are for consideration by source maintainers.*');
  lines.push('');

  return lines.join('\n');
}

