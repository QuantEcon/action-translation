/**
 * Generate a JSON report for a single file backward analysis
 */
export function generateJsonReport(report: BackwardReport): string {
  return JSON.stringify(report, null, 2);
}

/**
 * Generate a JSON report for bulk backward analysis
 */
export function generateBulkJsonReport(report: BulkBackwardReport): string {
  return JSON.stringify(report, null, 2);
}
